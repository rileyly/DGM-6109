let fahrenheit = 79 // 0C, 273.15K
// July in Washington, D.C. 
console.log("Temperature (Fahrenheit): " + fahrenheit + " degrees") 

// calculate celsius and store in celsius variable
// print that value

let celsius = (fahrenheit - 32) / 1.8
console.log("Temperature (Celsius): " + celsius + " degrees")

// calculate kelvin and store in variable
// print that value

let kelvin = (fahrenheit + 459.67) * 5 / 9
console.log("Temperature (Kelvin): " + kelvin + " degrees")
